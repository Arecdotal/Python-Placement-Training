''' a << b
    step - 1 Calculate the binary of a
    step - 2 Make it a near byte number
    step - 3 Remove b bits from the left side
    step - 4 Add b number of 0's at right side
    step - 5 Convert into decimal
'''
#LEFT SHIFT
print(4<<2)
print(9<<3)
print(3<<5)
print(13<<5)

#RIGHT SHIFT
print(13>>3)